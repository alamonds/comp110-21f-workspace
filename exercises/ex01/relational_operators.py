"""This is my relational operations pratice."""
x: int = int(input("Left-hand side: "))
y: int = int(input("Right-hand side: "))
print(str(x) + " < " + str(y) + " is " + str(x < y))
print(str(x) + " >= " + str(y) + " is " + str(x >= y))
print(str(x) + " == " + str(y) + " is " + str(x == y))
print(str(x) + " != " + str(y) + " is " + str(x != y))
